async function createArticle(articleData) {
  try {
    const response = await axios.post(`${STRAPI_URL}/api/articles`, {
      data: articleData
    }, {
      headers: {
        'Authorization': `Bearer ${API_TOKEN}`,
        'Content-Type': 'application/json'
      }
    });
    
    console.log(`✅ Статья создана: ${articleData.title}`);
    return response.data;
  } catch (error) {
    console.error(`❌ Ошибка при создании статьи "${articleData.title}":`, error.response?.data || error.message);
    return null;
  }
}

async function populateArticles() {
  console.log('🚀 Начинаем создание тестовых статей по AI...\n');
  
  if (!API_TOKEN) {
    console.log('⚠️  Внимание: STRAPI_API_TOKEN не установлен.');
    console.log('   Статьи будут созданы без авторизации (если настроен публичный доступ).\n');
  }
  
  let successCount = 0;
  
  for (const article of articles) {
    const result = await createArticle(article);
    if (result) {
      successCount++;
    }
    // Небольшая пауза между запросами
    await new Promise(resolve => setTimeout(resolve, 1000));
  }
  
  console.log(`\n📊 Результат: создано ${successCount} из ${articles.length} статей`);
  
  if (successCount > 0) {
    console.log('\n🌐 Проверьте статьи в Strapi Admin Panel: http://localhost:1337/admin');
    console.log('📝 Или через API: http://localhost:1337/api/articles');
  }
}

